void	render_column(t_ray	ray, int pos, double rel_angle)
{
	double		dist_perp;
	t_vector	column;
	static int	colors[] = {GREEN, D_GREEN, RED, D_RED};

	dist_perp = ray.distance * ft_cos(rel_angle);
	column.h = (W_HEIGHT * 20 / dist_perp);
	column.w = W_WIDTH / N_RAYS;
	column.x = pos * column.w;
	
	int			color;
	double		y;
	t_vector	t;
	double		ty_off;
	double		ty_step;

	t.x = (int) ray.cross.x % 32;
	if (ft_sin(ray.angle) < 0)
		t.x = (int) (31.0 - t.x);
	if (ray.vertical)
	{
		t.x = (int) ray.cross.y % 32;
		if (ft_cos(ray.angle) < 0)
			t.x = (int) (31.0 - t.x);
	}
	ty_off = 0;
	ty_step = 32.0 / (double) column.h;
	if (column.h >= W_HEIGHT)
	{
		ty_off = (column.h - W_HEIGHT) / 2.0;
		column.h = W_HEIGHT;
	}
	column.y = W_HEIGHT / 2 - column.h / 2;
	t.y = ty_off * ty_step;
	y = -1;
	while (++y < column.h)
	{
		color = GREEN;
		if (checkboard[(int) t.y * 32 + (int) t.x] == 0)
			color = RED;
		if (ray.vertical == 1)
			color *= 0.7;
		(canva())->rectangle(vector(column.x, column.y + y, column.w, 1), color);
		t.y += ty_step;
	}
}

char	get_ray_direction(t_ray ray, double angle)
{
	t_face	dir;

	if (ft_cos(angle) >= 0 && ray.vertical == 1)
		dir = W;
	if (ft_cos(angle) <= 0 && ray.vertical == 1)
		dir = E;
	if (ft_sin(angle) >= 0 && ray.vertical == 0)
		dir = S;
	if (ft_sin(angle) <= 0 && ray.vertical == 0)
		dir = N;
	return (dir);
}

void	render_view(t_player *p)
{
	double		rel_angle;
	t_ray		ray;
	int			color;
	int			i;

	rel_angle = VIEW_ANGLE / 2;
	i = -1;
	while (++i <= N_RAYS)
	{
		color = GREEN;
		ray = get_ray_return(p->vector, rel_angle);
		ray.direction = get_ray_direction(ray, p->vector.angle + rel_angle);
		render_column(ray, i, rel_angle);
		rel_angle -= (double) VIEW_ANGLE / N_RAYS;
	}
}
